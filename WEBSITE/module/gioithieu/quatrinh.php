      <h3 align="center">1993 – 2000: TĂNG TỐC MỞ RỘNG MẠNG LƯỚI VÀ NGÀNH NGHỀ<br />
      </h3>
      <div>
        <p>Năm 1994, PNJ thành lập Chi nhánh Hà Nội, mở đầu cho chiến lược mở rộng hệ thống tại các tỉnh, thành phố lớn trên toàn quốc về sau như Chi nhánh Đà Nẵng được khai trương năm 1998, Chi nhánh Cần Thơ được khai trương năm 1999, hệ thống phân phối PNJ luôn được mở rộng không ngừng.</p>
        <p>Giai đoạn này cũng mang đậm dấu ấn trong công tác xây dựng thương hiệu chuyên nghiệp của PNJ, nữ trang PNJ bắt đầu xuất khẩu. Chỉ sau vài năm từ lúc thành lập, trang sức thương hiệu PNJ nhanh chóng chiếm lĩnh thị trường, được người tiêu dùng tin yêu và khẳng định uy tín của mình. Với các danh hiệu và giải thưởng đạt được: Chứng nhận danh hiệu &ldquo;Hàng Việt Nam chất lượng cao&rdquo;, đón nhận Huân chương Lao động Hạng 3, Huân chương Lao động Hạng 2, PNJ còn được tổ chức DNV cấp chứng nhận Hệ thống Quản trị Chất lượng theo tiêu chuẩn ISO.</p>
