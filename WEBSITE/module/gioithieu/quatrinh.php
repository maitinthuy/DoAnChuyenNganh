      <h3 align="center">2005 – 2008: TÁI TUNG THƯƠNG HIỆU VÀ PHÁT TRIỂN NHÃN HÀNG CAO CẤP<br />
      </h3>
      <div>
        <p>Đây là giai đoạn phát triển khá mạnh mẽ của PNJ trên mọi mặt. Đặc biệt là trong công tác phát triển thương hiệu và sản phẩm mới, được đánh dấu bằng các sự kiện PNJSilver tái tung hình ảnh mới. Nhãn hiệu trang sức CAO Fine Jewellery chính thức ra đời. PNJ là nhà tài trợ trang sức và vương miện cho cuộc thi Hoa hậu Hoàn vũ 2008 tổ chức tại Việt Nam. Và ngày 3/4/2008, PNJ chính thức công bố thay đổi logo mới nhân kỷ niệm 20 năm thành lập và phát triển với hình ảnh chuyên nghiệp, hiện đại hơn, xứng tầm cho chặng đường mới.</p>
        <p>PNJ trở thành một trong 200 doanh nghiệp lớn nhất Việt Nam theo công bố của UNDP và vinh dự đón nhận danh hiệu cao quý do Chủ tịch nước trao tặng: Huân chương Độc lập Hạng ba.</p>
