      <h3 align="center">1988 -1992: HÌNH THÀNH VÀ XÁC ĐỊNH CHIẾN LƯỢC PHÁT TRIỂN<br />
      </h3>
      <div>
        <p>Ngày 28/04/1988, cửa hàng Kinh doanh Vàng bạc Phú Nhuận ra đời, trực thuộc UBND Quận Phú Nhuận trong thời điểm ngành kim hoàn Việt Nam còn non trẻ, chưa phát triển, thị trường còn nhiều hạn chế chính là những thách thức lớn mà 20 con người đầu tiên sáng lập ra PNJ phải vượt qua.</p>
        <p>Năm 1992, PNJ chính thức mang tên Công ty Vàng Bạc Đá Quý Phú Nhuận sau hai lần đổi tên từ Cửa hàng Kinh doanh Vàng Bạc Phú Nhuận và Công ty Vàng Bạc Mỹ Nghệ Kiều Hối Phú Nhuận, đồng thời xác định chiến lược phát triển là nhà sản xuất kinh doanh trang sức chuyên nghiệp. Tại thời điểm này, PNJ còn mở rộng hoạt động bằng việc sáng lập Ngân hàng Đông Á với tỷ lệ vốn góp 40%.</p>
