      <h3 align="center">2012 – 2017:  TÁI CẤU TRÚC ĐỂ PHÁT TRIỂN TRƯỜNG TỒN<br />
      </h3>
      <p>Xác định cần phải tái cơ cấu lại chiến lược và bộ máy hoạt động để phát triển trường tồn, năm 2012, PNJ thuê tư vấn nước ngoài để chuẩn hóa hệ thống quản trị theo chuẩn quốc tế. Trước khi mời tư vấn vào, PNJ đã tổ chức những buổi hội thảo để mọi người thấy được sự cần thiết của sự thay đổi. PNJ đã nhận ra những điều chưa tốt đang cản trở phát triển và kết luận phải thay đổi để không tụt hậu.</p>
      <p>Ngày <strong>18/10/2012</strong>, PNJ đã khánh thành Xí nghiệp nữ trang PNJ sau thời gian gần 18 tháng thi công. Với tổng vốn đầu tư là 120 tỷ đồng, có công suất sản xuất đạt trên 4 triệu sản phẩm/năm, XN Nữ trang PNJ được đánh giá là một trong những xí nghiệp chế tác nữ trang lớn nhất khu vực Châu Á.</p>
      <p>Năm 2013 là năm đánh dấu những sư kiện quan trọng trong chiến lược phát triển thương hiệu của PNJ. Ngày<strong>12/1/2013</strong>, PNJ đã khánh thành Trung tâm trang sức, kim cương và đồng hồ lớn nhất trên toàn hệ thống tại 52A- 52B Nguyễn Văn Trỗi, Q. Phú Nhuận, TP Hồ Chí Minh, được xem là một trong những trung tâm trang sức, kim cương lớn nhất tại thị trường Việt Nam.</p>
      <p>Ngày <strong>10/09/2013</strong>, PNJSilver đã chính thức tái định vị nhãn hàng, ra mắt bộ nhận diện thương hiệu mới,  mở ra một &ldquo;Thế giới Nàng tỏa sáng&rdquo; với sắc tím thời trang cùng những đột phá trong chiến lược phát triển sản phẩm và thương hiệu. Song song đó, thương hiệu trang sức vàng PNJ công bố thông điệp mới &ldquo;Tôn vinh giá trị đích thực&rdquo; với mục đích tôn vinh những giá trị &ldquo;vàng&rdquo;, những phẩm chất cao quý của người phụ nữ trong xã hội hiện đại ngày nay trên nền tảng kế thừa hài hòa với các giá trị truyền thống của người phụ nữ Việt Nam.</p>
      <p>Năm 2014, PNJ đã mở hàng loạt TTKH ở các tỉnh thành VN …nâng tổng số cửa hàng bán lẻ trang sức lên đến gần 200 cửa hàng trong toàn quốc.</p>
      <p>Kết quả kinh doanh vượt trội của năm 2014 chính là kết quả tái cấu trúc công ty thành công, hướng đến mục tiêu phát triển trường tồn, đưa PNJ trở thành công ty chế tác và bán lẻ trang sức </p>
