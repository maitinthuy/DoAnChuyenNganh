      <h3 align="center">2001 – 2004: ĐẨY MẠNH XÂY DỰNG THƯƠNG HIỆU VÀ THỰC HIỆN CỔ PHẦN HOÁ<br />
      </h3>
      <div>
        <p>Năm 2001, Nhãn hiệu PNJSilver chính thức ra đời đáp ứng nhu cầu làm đẹp cho các bạn trẻ yêu thích trang sức song song với việc tiếp tục phát triển mạnh mẽ nhãn hàng trang sức vàng PNJ bằng nhiều chương trình tiếp thị gây tiếng vang với sự bảo trợ của Hội đồng vàng thế giới.</p>
        <p>Ngày 2/1/2004: PNJ chính thức cổ phần hóa, chuyển đổi từ doanh nghiệp kinh tế Đảng thành Công ty Cổ phần Vàng Bạc Đá Quý Phú Nhuận. Cũng trong thời gian này, PNJ đạt danh hiệu Top 500 Doanh nghiệp bán lẻ hàng đầu Châu Á – Thái Bình Dương và đón nhận Huân chương Lao động Hạng nhất.</p>
