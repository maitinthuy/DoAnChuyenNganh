      <p>Tháng <strong>3/2009</strong>, cổ phiếu PNJ chính thức niêm yết tại HOSE, tăng vốn điều lệ lên 400 tỷ đồng.</p>
      <p>Tháng 8/2009 thành lập Công ty TNHH MTV Thời trang CAO cùng với việc bổ sung ngành kinh doanh đồng hồ của các nhãn hiệu nổi tiếng thế giới tại hệ thống PNJ. PNJ cũng là nhà tài trợ trang sức cho cuộc thi Hoa hậu Trái đất năm 2010 được tổ chức tại Việt Nam. Với 142 cửa hàng, PNJ trở thành công ty có hệ thống bán lẻ trang sức lớn nhất Việt Nam.</p>
      <p>Năm 2010, PNJ được Chính phủ công nhận là thương hiệu quốc gia, đón nhận giải vàng Chất lượng quốc gia, là 1 trong 3 doanh nghiệp Việt Nam nhận giải thưởng chất lượng châu Á – Thái Bình Dương và được Plimsoll công bố là công ty xếp thứ 16 trong top 500 công ty nữ trang lớn nhất thế giới. PNJ là công ty Việt Nam duy nhất có mặt trong bảng xếp hạng này.</p>
      <p>Tháng 3/2011, PNJ khởi công xây dựng Xí nghiệp Nữ trang PNJ hiện đại nhất Việt Nam, đồng thời không ngừng nâng cấp quy mô hệ thống phân phối, khánh thành các trụ sở và khai trương các trung tâm kim hoàn tại Kiên Giang, Bình Dương, Biên Hòa, Hà Nội….</p>
      <p>Thời điểm 2011 cũng là lúc PNJ chuẩn bị xây dựng chiến lược cho giai đoạn 2012-2017.</p>
